/**
 * –≠–∫—Ä–∞–Ω –¥–ª—è –¥–µ–º–æ–Ω—Å—Ç—Ä–∞—Ü–∏–∏ —Ä–∞–±–æ—Ç—ã –º–∏–∫—Ä–æ-JUnit –¥–≤–∏–∂–∫–∞
 */
@Composable
fun TestingScreen(
    modifier: Modifier = Modifier,
    onBackPressed: () -> Unit = {}
) {
    val context = androidx.compose.ui.platform.LocalContext.current
    var selectedClass by remember { mutableStateOf<Class<*>?>(null) }
    var testResults by remember { mutableStateOf<MicroJUnitTestResult?>(null) }
    var isLoading by remember { mutableStateOf(false) }
    var isGeneratingTests by remember { mutableStateOf(false) }
    var generatedTestCode by remember { mutableStateOf("") }
    
    // –°–æ—Å—Ç–æ—è–Ω–∏–µ –¥–ª—è –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏—è –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏
    var isAppSettingsInitialized by remember { mutableStateOf(false) }
    
    // –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º AppSettings
    LaunchedEffect(context) {
        AppSettings.initialize(context)
        isAppSettingsInitialized = true
    }
    
    // –°–æ–∑–¥–∞–µ–º –∞–≥–µ–Ω—Ç –¥–ª—è –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ –∏ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è —Ç–µ—Å—Ç–æ–≤ —Ç–æ–ª—å–∫–æ –ø–æ—Å–ª–µ –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏
    val llmTestAgent = remember(isAppSettingsInitialized) {
        if (isAppSettingsInitialized) {
            val apiKey = AppSettings.getApiKey()
            println("üîë TestingScreen: API –∫–ª—é—á –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –∞–≥–µ–Ω—Ç–∞: '${if (apiKey.isBlank()) "–ø—É—Å—Ç–æ–π" else "—É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω (–¥–ª–∏–Ω–∞: ${apiKey.length})"}'")
            
            LLMTestGeneratorAgentRepository(
                apiKey = apiKey,
                networkProvider = NetworkModule,
                microJUnitEngine = MicroJUnitEngine()
            )
        } else {
            println("‚è≥ TestingScreen: AppSettings –µ—â–µ –Ω–µ –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä–æ–≤–∞–Ω")
            null
        }
    }
    
    val coroutineScope = rememberCoroutineScope()
    
    val availableClasses = listOf(
        Calculator::class.java
    )
    
    Column(
        modifier = modifier
            .fillMaxSize()
            .padding(16.dp)
    ) {
        Row(
            modifier = Modifier.fillMaxWidth(),
            verticalAlignment = Alignment.CenterVertically
        ) {
            IconButton(
                onClick = onBackPressed,
                colors = IconButtonDefaults.iconButtonColors(
                    containerColor = MaterialTheme.colorScheme.primaryContainer,
                    contentColor = MaterialTheme.colorScheme.onPrimaryContainer
                )
            ) {
                                                Icon(
                                    imageVector = Icons.AutoMirrored.Filled.ArrowBack,
                                    contentDescription = "–ù–∞–∑–∞–¥"
                                )
            }
            
            Spacer(modifier = Modifier.width(16.dp))
        }
        
        // –í—ã–±–æ—Ä –∫–ª–∞—Å—Å–∞ –¥–ª—è —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏—è
        Card(
            modifier = Modifier.fillMaxWidth(),
            elevation = CardDefaults.cardElevation(defaultElevation = 4.dp)
        ) {
            Column(
                modifier = Modifier.padding(16.dp)
            ) {
                Text(
                    text = "–í—ã–±–µ—Ä–∏—Ç–µ –∫–ª–∞—Å—Å –¥–ª—è —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏—è:",
                    style = MaterialTheme.typography.titleMedium,
                    modifier = Modifier.padding(bottom = 8.dp)
                )
                
                availableClasses.forEach { clazz ->
                    Row(
                        modifier = Modifier
                            .fillMaxWidth()
                            .padding(vertical = 4.dp),
                        verticalAlignment = Alignment.CenterVertically
                    ) {
                        RadioButton(
                            selected = selectedClass == clazz,
                            onClick = { selectedClass = clazz }
                        )
                        Text(
                            text = clazz.simpleName,
                            modifier = Modifier.padding(start = 8.dp)
                        )
                    }
                }
            }
        }
        
        Spacer(modifier = Modifier.height(16.dp))
        
        // –ö–Ω–æ–ø–∫–∏ –¥–µ–π—Å—Ç–≤–∏–π
        Row(
            modifier = Modifier.fillMaxWidth(),
            horizontalArrangement = Arrangement.spacedBy(8.dp)
        ) {
            Button(
                onClick = {
                    selectedClass?.let { clazz ->
                        llmTestAgent?.let { agent ->
                            isGeneratingTests = true
                            testResults = null // –°–±—Ä–∞—Å—ã–≤–∞–µ–º —Å—Ç–∞—Ä—ã–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã
                            
                            coroutineScope.launch {
                                try {
                                    println("ü§ñ –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º —Ç–µ—Å—Ç—ã —á–µ—Ä–µ–∑ LLM –¥–ª—è ${clazz.simpleName}")
                                    
                                    // –°–æ–∑–¥–∞–µ–º –ø—Ä–æ–º–ø—Ç –¥–ª—è LLM
                                    val prompt = agent.createLLMPrompt(
                                        targetClass = clazz,
                                        testRequirements = "–°–æ–∑–¥–∞–π JUnit —Ç–µ—Å—Ç—ã –¥–ª—è –≤—Å–µ—Ö –ø—É–±–ª–∏—á–Ω—ã—Ö –º–µ—Ç–æ–¥–æ–≤ –∫–ª–∞—Å—Å–∞ ${clazz.simpleName}. –ò—Å–ø–æ–ª—å–∑—É–π assertEquals –∏ –∫–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä—ã."
                                    )
                                    
                                    // –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –∏ –≤—ã–ø–æ–ª–Ω—è–µ–º —Ç–µ—Å—Ç—ã
                                    val result = agent.generateAndRunTests(clazz, prompt)
                                    
                                    result.fold(
                                        onSuccess = { testResult ->
                                            println("‚úÖ LLM —Ç–µ—Å—Ç—ã –≤—ã–ø–æ–ª–Ω–µ–Ω—ã —É—Å–ø–µ—à–Ω–æ!")
                                            testResults = testResult
                                            generatedTestCode = "–¢–µ—Å—Ç—ã —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω—ã LLM –∏ –≤—ã–ø–æ–ª–Ω–µ–Ω—ã —É—Å–ø–µ—à–Ω–æ"
                                        },
                                        onFailure = { error ->
                                            println("‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏/–≤—ã–ø–æ–ª–Ω–µ–Ω–∏–∏ LLM —Ç–µ—Å—Ç–æ–≤: ${error.message}")
                                            generatedTestCode = "–û—à–∏–±–∫–∞: ${error.message}"
                                        }
                                    )
                                } catch (e: Exception) {
                                    println("üí• –ù–µ–æ–∂–∏–¥–∞–Ω–Ω–∞—è –æ—à–∏–±–∫–∞: ${e.message}")
                                    generatedTestCode = "–ù–µ–æ–∂–∏–¥–∞–Ω–Ω–∞—è –æ—à–∏–±–∫–∞: ${e.message}"
                                } finally {
                                    isGeneratingTests = false
                                }
                            }
                        } ?: run {
                            println("‚ö†Ô∏è LLM –∞–≥–µ–Ω—Ç –µ—â–µ –Ω–µ –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä–æ–≤–∞–Ω")
                            generatedTestCode = "–û—à–∏–±–∫–∞: LLM –∞–≥–µ–Ω—Ç –µ—â–µ –Ω–µ –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä–æ–≤–∞–Ω"
                        }
                    }
                },
                enabled = selectedClass != null && !isGeneratingTests && llmTestAgent != null,
                modifier = Modifier.weight(1f)
            ) {
                if (isGeneratingTests) {
                    Row(
                        horizontalArrangement = Arrangement.spacedBy(8.dp),
                        verticalAlignment = Alignment.CenterVertically
                    ) {
                        CircularProgressIndicator(
                            modifier = Modifier.size(16.dp),
                            strokeWidth = 2.dp,
                            color = MaterialTheme.colorScheme.onPrimary
                        )
                        Text("ü§ñ LLM –≥–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç...")
                    }
                } else {
                    Text("ü§ñ –ì–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞—Ç—å —Ç–µ—Å—Ç—ã LLM")
                }
            }
            
            // –ö–Ω–æ–ø–∫–∞ "–°–±—Ä–æ—Å–∏—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã" –¥–ª—è –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç–∏ –ø–æ–≤—Ç–æ—Ä–Ω–æ–≥–æ –∑–∞–ø—É—Å–∫–∞
            Button(
                onClick = {
                    testResults = null
                    generatedTestCode = ""
                },
                enabled = testResults != null && !isGeneratingTests,
                modifier = Modifier.weight(1f),
                colors = ButtonDefaults.buttonColors(
                    containerColor = MaterialTheme.colorScheme.secondary
                )
            ) {
                Text("üîÑ –°–±—Ä–æ—Å–∏—Ç—å")
            }
        }
        
        Spacer(modifier = Modifier.height(16.dp))
        
        // –°—Ç–∞—Ç—É—Å –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ (–ø–æ–∫–∞–∑—ã–≤–∞–µ–º –∫–æ–≥–¥–∞ –∏–¥–µ—Ç –≥–µ–Ω–µ—Ä–∞—Ü–∏—è –∏–ª–∏ –µ—Å—Ç—å –æ—à–∏–±–∫–∞)
        if (isGeneratingTests) {
            Card(
                modifier = Modifier.fillMaxWidth(),
                elevation = CardDefaults.cardElevation(defaultElevation = 4.dp)
            ) {
                Column(
                    modifier = Modifier.padding(16.dp),
                    horizontalAlignment = Alignment.CenterHorizontally
                ) {
                    CircularProgressIndicator()
                    Spacer(modifier = Modifier.height(8.dp))
                    Text(
                        text = "ü§ñ LLM –≥–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –∏ –≤—ã–ø–æ–ª–Ω—è–µ—Ç —Ç–µ—Å—Ç—ã...",
                        style = MaterialTheme.typography.titleMedium
                    )
                }
            }
            Spacer(modifier = Modifier.height(16.dp))
        } else if (generatedTestCode.isNotEmpty() && testResults == null) {
            Card(
                modifier = Modifier.fillMaxWidth(),
                elevation = CardDefaults.cardElevation(defaultElevation = 4.dp)
            ) {
                Column(
                    modifier = Modifier.padding(16.dp)
                ) {
                    Text(
                        text = "üìã –°–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ —Ç–µ—Å—Ç—ã:",
                        style = MaterialTheme.typography.titleMedium,
                        modifier = Modifier.padding(bottom = 8.dp)
                    )
                    
                    LazyColumn(
                        modifier = Modifier
                            .fillMaxWidth()
                            .height(200.dp)
                            .background(
                                MaterialTheme.colorScheme.surfaceVariant,
                                shape = MaterialTheme.shapes.small
                            )
                            .padding(8.dp)
                    ) {
                        item {
                            Text(
                                text = generatedTestCode,
                                fontFamily = FontFamily.Monospace,
                                fontSize = 12.sp
                            )
                        }
                    }
                }
            }
            
            Spacer(modifier = Modifier.height(16.dp))
        }
        
        // –†–µ–∑—É–ª—å—Ç–∞—Ç—ã —Ç–µ—Å—Ç–æ–≤ (–ø–æ–∫–∞–∑—ã–≤–∞–µ–º –ø–æ—Å–ª–µ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è)
        if (testResults != null) {
            val results = testResults!!
            Card(
                modifier = Modifier.fillMaxWidth(),
                elevation = CardDefaults.cardElevation(defaultElevation = 4.dp)
            ) {
                Column(
                    modifier = Modifier.padding(16.dp)
                ) {
                    Text(
                        text = "üìä –†–µ–∑—É–ª—å—Ç–∞—Ç—ã —Ç–µ—Å—Ç–æ–≤:",
                        style = MaterialTheme.typography.titleMedium,
                        modifier = Modifier.padding(bottom = 8.dp)
                    )
                    
                    // –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞
                    Row(
                        modifier = Modifier.fillMaxWidth(),
                        horizontalArrangement = Arrangement.SpaceEvenly
                    ) {
                        StatisticCard(
                            title = "–í—Å–µ–≥–æ",
                            value = results.testCount.toString(),
                            color = MaterialTheme.colorScheme.primary
                        )
                        StatisticCard(
                            title = "–ü—Ä–æ–π–¥–µ–Ω–æ",
                            value = results.passedTests.toString(),
                            color = MaterialTheme.colorScheme.tertiary
                        )
                        StatisticCard(
                            title = "–ü—Ä–æ–≤–∞–ª–µ–Ω–æ",
                            value = results.failedTests.toString(),
                            color = MaterialTheme.colorScheme.error
                        )
                    }
                    
                    Spacer(modifier = Modifier.height(16.dp))
                    
                    // –î–µ—Ç–∞–ª—å–Ω—ã–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã
                    Text(
                        text = "–î–µ—Ç–∞–ª–∏:",
                        style = MaterialTheme.typography.titleSmall,
                        modifier = Modifier.padding(bottom = 8.dp)
                    )
                    
                    LazyColumn(
                        modifier = Modifier.height(200.dp)
                    ) {
                        items(results.testResults) { testResult ->
                            TestResultItem(testResult = testResult)
                        }
                    }
                    
                    Spacer(modifier = Modifier.height(16.dp))
                    
                    // –û–±—â–∏–π —Å—Ç–∞—Ç—É—Å
                    Card(
                        modifier = Modifier.fillMaxWidth(),
                        colors = CardDefaults.cardColors(
                            containerColor = if (results.success) 
                                MaterialTheme.colorScheme.tertiaryContainer 
                            else 
                                MaterialTheme.colorScheme.errorContainer
                        )
                    ) {
                        Text(
                            text = if (results.success) "‚úÖ –í—Å–µ —Ç–µ—Å—Ç—ã –ø—Ä–æ—à–ª–∏ —É—Å–ø–µ—à–Ω–æ!" else "‚ùå –ï—Å—Ç—å –ø—Ä–æ–≤–∞–ª–µ–Ω–Ω—ã–µ —Ç–µ—Å—Ç—ã",
                            style = MaterialTheme.typography.titleMedium,
                            textAlign = TextAlign.Center,
                            modifier = Modifier
                                .fillMaxWidth()
                                .padding(16.dp)
                        )
                    }
                }
            }
        }
        
        if (isLoading) {
            Box(
                modifier = Modifier.fillMaxSize(),
                contentAlignment = Alignment.Center
            ) {
                CircularProgressIndicator()
            }
        }
    }
}

@Composable
private fun StatisticCard(
    title: String,
    value: String,
    color: androidx.compose.ui.graphics.Color
) {
    Card(
        colors = CardDefaults.cardColors(containerColor = color.copy(alpha = 0.1f)),
        modifier = Modifier.padding(4.dp)
    ) {
        Column(
            modifier = Modifier.padding(8.dp),
            horizontalAlignment = Alignment.CenterHorizontally
        ) {
            Text(
                text = value,
                style = MaterialTheme.typography.headlineSmall,
                color = color
            )
            Text(
                text = title,
                style = MaterialTheme.typography.bodySmall,
                color = color
            )
        }
    }
}

@Composable
private fun TestResultItem(
    testResult: MicroJUnitSingleTestResult
) {
    Card(
        modifier = Modifier
            .fillMaxWidth()
            .padding(vertical = 2.dp),
        colors = CardDefaults.cardColors(
            containerColor = if (testResult.success) 
                MaterialTheme.colorScheme.tertiaryContainer.copy(alpha = 0.3f)
            else 
                MaterialTheme.colorScheme.errorContainer.copy(alpha = 0.3f)
        )
    ) {
        Column(
            modifier = Modifier.padding(8.dp)
        ) {
            Row(
                verticalAlignment = Alignment.CenterVertically
            ) {
                Text(
                    text = if (testResult.success) "‚úÖ" else "‚ùå",
                    fontSize = 16.sp
                )
                Text(
                    text = testResult.testName,
                    style = MaterialTheme.typography.bodyMedium,
                    modifier = Modifier.padding(start = 8.dp)
                )
                Spacer(modifier = Modifier.weight(1f))
                testResult.duration?.let { duration ->
                    Text(
                        text = "${duration}ms",
                        style = MaterialTheme.typography.bodySmall,
                        color = MaterialTheme.colorScheme.onSurfaceVariant
                    )
                }
            }
            
            if (!testResult.success && testResult.errorMessage != null) {
                Text(
                    text = "–û—à–∏–±–∫–∞: ${testResult.errorMessage}",
                    style = MaterialTheme.typography.bodySmall,
                    color = MaterialTheme.colorScheme.error,
                    modifier = Modifier.padding(start = 24.dp, top = 4.dp)
                )
            }
            
            // –†–µ–∑—É–ª—å—Ç–∞—Ç—ã assert'–æ–≤
            if (testResult.assertionResults.isNotEmpty()) {
                testResult.assertionResults.forEach { assertion ->
                    Row(
                        modifier = Modifier.padding(start = 24.dp, top = 2.dp),
                        verticalAlignment = Alignment.CenterVertically
                    ) {
                        Text(
                            text = if (assertion.success) "‚úì" else "‚úó",
                            fontSize = 12.sp,
                            color = if (assertion.success) 
                                MaterialTheme.colorScheme.tertiary 
                            else 
                                MaterialTheme.colorScheme.error
                        )
                        Text(
                            text = assertion.message,
                            style = MaterialTheme.typography.bodySmall,
                            modifier = Modifier.padding(start = 4.dp)
                        )
                    }
                }
            }
        }
    }
}


